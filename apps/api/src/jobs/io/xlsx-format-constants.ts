// Boolean attributes are three-state (true/false/undefined). Write them as 'Yes'/'No' text
// rather than native booleans: spreadsheet applications rewrite boolean cells as `=TRUE()`/
// `=FALSE()` formulas, and ExcelJS drops the cached result of a `=FALSE()` formula, which made
// `false` indistinguishable from an empty cell on re-import. The reader accepts yes/no/y/n/
// true/false, so a blank cell still round-trips as undefined.
export function xlsxBoolean(value: boolean | undefined | null): 'Yes' | 'No' | '' {
  if (value === undefined || value === null)
    return '';

  return value ? 'Yes' : 'No';
}

